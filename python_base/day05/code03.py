# 如何创建空元组
t01 = ()
t02 = tuple()

# 创建具有默认值的元组
t01 = (1,2,3)
t02 = tuple("abcd")
print(t01)
print(t02)

# 修改
# 元组元素不能修改，但是元组中的列表的元素可以修改
# t01[2] = 6

# 获取元素
print(t01[:2])

# 获取元组所有元素
print(t01)#仅仅是将元组转换成字符串

for item in t01:
    print(item)

# 倒叙获取元组所有元素
for i in range(len(t01) - 1 ,-1,-1):
    print(t01[i])


# 元组创建后，若之后代码具有对copy后的元组进行增删的操作，原元组会跟随变化

# 如果元组只有一个元素，必须多写一个逗号，否则视为普通对象，非元组对象